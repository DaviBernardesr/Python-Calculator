def adicao(num1, num2):
    result = num1 + num2
    return result

def subtracao(num1, num2):
    result = num1 - num2
    return result

def multiply(num1, num2):
    result = num1 * num2
    return result

def poten(num1, num2):
    result = num1 ** num2
    return result

def divis(num1, num2):
    result = num1 / num2
    return result
